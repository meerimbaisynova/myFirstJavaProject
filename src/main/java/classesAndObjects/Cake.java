package classesAndObjects;

public class Cake {

    String name;
    boolean hasGluten;
    String form;
    String flavor;
    double price;
    int calories;


    public String getName(){
        return name;
    }

    public boolean isGlutenFree(){
        return hasGluten;
    }

    public String getForm(){
        return form;
    }

    public String getFlavor() {
        return flavor;
    }

    public double getPrice() {
        return price;
    }

    public int getCalories() {
        return calories;
    }


}

